    private void buttonBuscaActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_buttonBuscaActionPerformed
        Venda ven = new Venda();
        List<Venda> vend = new ArrayList();
        String[] tabela = new String[]{"IDNOTAFISCAL", "DATAVENDA", "PRECO", "ID FUNCIONARIO"};
        DefaultTableModel model = new DefaultTableModel(null, tabela);
        vendaTable.setModel(model);
        try{
            vend.forEach(ven1->{
                DateFormat dateFormat = new SimpleDateFormat("yyyy-mm-dd hh:mm:ss");  
                String strDate = dateFormat.format(ven.getDataVenda());
                model.addRow(new String[]{Integer.toString(ven1.getIDNotaFiscal()), strDate, Double.toString(ven.getPreco()), Integer.toString(ven.getIdFun())});
            });
        }
        catch(RuntimeException e){
            JOptionPane.showMessageDialog(null, e);
        }
    }//GEN-LAST:event_buttonBuscaActionPerformed

